package study01.test02;

public class NumberDataType {

	public static void main(String[] agrs) {
		// 제일 작음 (1byte)
		byte b = 127;
		// byte보다 큼(2byte)
		short s = 128;
		// short보다 큼(3byte)
		int i = 100000;
		// 가장 큼(4byte)
		long l = 100000000;

		double d = 1;
		float f = 0.000001f;

	}
}
